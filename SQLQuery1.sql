SELECT E.FNAME+' '+E.LNAME AS EMPLOYEE_NAME, P.PNAME AS PROJECT_NAME
	FROM EMPLOYEE E
LEFT JOIN Works_for W
	ON E.SSN = W.ESSn
LEFT JOIN PROJECT P
	ON W.Pno = P.PNUMBER
ORDER BY PROJECT_NAME

