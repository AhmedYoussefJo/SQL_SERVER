SELECT P.PNUMBER, D.DNAME + ' ' + E.LNAME AS MGRNAME, E.ADDRESS, E.BDATE
FROM EMPLOYEE E
INNER JOIN WORKS_FOR W
ON E.SSN = W.ESSn
INNER JOIN PROJECT P
ON W.Pno = P.PNUMBER
INNER JOIN DEPARTMENTS D
ON P.DNUM = D.DNUM
WHERE D.MGRSSN = E.SSN

